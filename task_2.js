/*Я просто використав функцію includes(), котра повертає true, якщо стрічка входить в задану стрічку і 
false, якщо не входить*/
function IfIncludes(text,word){
    console.log(`Стрічка "${word}" ${text.includes(word) ? 'входить' : 'не входить'} в речення "${text}";`);
}

IfIncludes('В мене погана фантазія','фантазія');
IfIncludes('В цьому реченні не буде шуканого слова','машина');
IfIncludes('Ну і останній приклад','а');